# Zadanie: System zarządzania książkami w bibliotece

# Napisz program, który pozwala użytkownikowi zarządzać prostą bazą danych książek w bibliotece. 
# Program powinien posiadać następujące funkcje:

# Dodawanie książki:
# Funkcja dodaj_ksiazke powinna umożliwiać dodanie książki (tytuł i autor) do bazy danych.

# Wyświetlanie książek:
# Funkcja wyswietl_ksiazki powinna wyświetlać wszystkie książki w bibliotece.

# Wyszukiwanie książki:
# Funkcja szukaj_ksiazki powinna wyszukiwać książkę w bazie danych na podstawie tytułu.

# Usuwanie książki:
# Funkcja usun_ksiazke powinna usuwać książkę na podstawie tytułu, jeśli istnieje w bazie danych.

# Obsługa wyjątków:
# Obsłuż sytuację, gdy użytkownik wprowadzi dane niezgodne z oczekiwanym formatem (np. brak tytułu).
# Obsłuż sytuację, gdy użytkownik próbuje usunąć książkę, która nie istnieje.

# Menu:
# Program powinien działać w pętli i umożliwiać użytkownikowi wybór jednej z powyższych opcji.